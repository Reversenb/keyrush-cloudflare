// อักขระล่องหน/ควบคุม ที่คนใช้แทรกเพื่อเลี่ยงตัวกรอง
const INVISIBLE = /[​-\u200F\u202A-\u202E﻿­]/g
// สระ/วรรณยุกต์ไทยที่ลอยอยู่บน-ล่าง (แทรกเยอะๆ เพื่อเลี่ยงตัวกรองได้)
const TH_MARKS = /[ัิ-ฺ็-๎]/g

// แปลงตัวเลข/สัญลักษณ์ที่ใช้แทนตัวอักษร (leetspeak): f4ck → fuck
const LEET: Record<string, string> = {
  '4': 'a', '@': 'a', '8': 'b', '3': 'e', '1': 'i', '!': 'i',
  '0': 'o', '5': 's', '$': 's', '7': 't', '+': 't',
}

// ล้างข้อความให้เหลือรูปเดียว: ตัดช่องว่าง/สัญลักษณ์ทิ้ง + ยุบตัวซ้ำ
// "ค ว ย" / "ค.ว.ย" / "ควยยยย" → "ควย" ทั้งหมด
function squash(text: string): string {
  return text
    .toLowerCase()
    .replace(INVISIBLE, '')
    .replace(/[0-9@!$+]/g, (ch) => LEET[ch] ?? '')
    .replace(/[^a-z฀-๿]/g, '')  // เหลือแค่ a-z กับอักษรไทย
    .replace(/(.)\1+/g, '$1')             // ยุบตัวซ้ำติดกันให้เหลือตัวเดียว
}

// แยกเป็นคำๆ ไว้เทียบคำอังกฤษสั้นที่ต้องตรงทั้งคำ
function tokenize(text: string): string[] {
  return text
    .toLowerCase()
    .replace(INVISIBLE, '')
    .replace(/[0-9@!$+]/g, (ch) => LEET[ch] ?? '')
    .split(/[^a-z]+/)
    .filter(Boolean)
}

// เตรียมลิสต์ให้อยู่ในรูปเดียวกับข้อความที่ผ่าน squash แล้ว (dedupe ด้วย เผื่อในลิสต์มีคำซ้ำ)
const uniq = (arr: string[]) => Array.from(new Set(arr.map(squash).filter(Boolean)))
const TH_BAD_N = uniq(TH_BAD)
const TH_SAFE_N = uniq(TH_SAFE)
const EN_SUBSTR_N = uniq(EN_SUBSTR)
const RESERVED_N = uniq(RESERVED)

// ลิสต์สำหรับ "รอบตัดวรรณยุกต์" — ใช้ได้เฉพาะคำที่ไม่มีวรรณยุกต์อยู่แล้ว และยาว 3 ตัวขึ้นไป
// ⚠️ ถ้าเอาคำที่มีวรรณยุกต์มาตัดด้วย คำสั้นจะเหลือพยัญชนะตัวเดียว (เช่น "หี" → "ห")
//    แล้วไปแมตช์คำปกติทุกคำที่มี ห (ทำอาหาร / หน้าใหม่ / ทหาร) — เคยพลาดตรงนี้มาแล้ว
const TH_BAD_NOMARK = TH_BAD_N.filter((w) => w.replace(TH_MARKS, '') === w && w.length >= 3)
const TH_SAFE_NOMARK = TH_SAFE_N.map((w) => w.replace(TH_MARKS, '')).filter((w) => w.length >= 3)

// หาคำต้องห้ามในข้อความ โดยข้ามตำแหน่งที่จริงๆ แล้วเป็นส่วนของคำสุภาพ
function findBad(haystack: string, words: string[], safeWords: string[]): boolean {
  for (const w of words) {
    if (!w) continue
    let i = haystack.indexOf(w)
    while (i !== -1) {
      // ตำแหน่งนี้เป็นส่วนหนึ่งของคำสุภาพหรือเปล่า? (เช่น "หี" ที่อยู่ใน "หีบ")
      const insideSafeWord = safeWords.some((safe) => {
        const offset = safe.indexOf(w)
        if (offset === -1) return false
        const start = i - offset
        return start >= 0 && haystack.substr(start, safe.length) === safe
      })
      if (!insideSafeWord) return true
      i = haystack.indexOf(w, i + 1)
    }
  }
  return false
}

/**
 * ตรวจข้อความ — คืนข้อความแจ้งเตือนถ้าไม่ผ่าน, คืน null ถ้าผ่าน
 * @param text ข้อความที่ผู้ใช้กรอก
 * @param opts.label ชื่อช่องที่จะใช้ในข้อความแจ้งเตือน (เช่น "ชื่อเล่น")
 * @param opts.checkReserved เช็คชื่อสงวนด้วยไหม (ใช้กับชื่อเล่น ไม่ใช้กับ bio)
 */
export function checkProfanity(
  text: string | undefined | null,
  opts: { label: string; checkReserved?: boolean }
): string | null {
  if (!text || !text.trim()) return null

  const squashed = squash(text)
  if (!squashed) return null

  // รูปที่ตัดสระ/วรรณยุกต์ไทยออก — จับกรณีแทรกวรรณยุกต์รัวๆ เพื่อเลี่ยง เช่น "ค๋ว๋ย๋"
  const stripped = squashed.replace(TH_MARKS, '')

  const badTh =
    findBad(squashed, TH_BAD_N, TH_SAFE_N) ||
    findBad(stripped, TH_BAD_NOMARK, TH_SAFE_NOMARK)

  const badEn = findBad(squashed, EN_SUBSTR_N, [])
  const badToken = tokenize(text).some((t) => EN_TOKEN.includes(t))

  if (badTh || badEn || badToken) {
    return `${opts.label}มีคำไม่สุภาพ กรุณาแก้ไขใหม่`
  }

  if (opts.checkReserved && RESERVED_N.some((r) => squashed === r || squashed.includes(r))) {
    return `${opts.label}นี้เป็นชื่อสงวนของระบบ กรุณาใช้ชื่ออื่น`
  }

  return null
}
